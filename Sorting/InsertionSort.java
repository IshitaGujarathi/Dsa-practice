import java.util.*;
public class InsertionSort{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the value of n :");
        int n = sc.nextInt();

        int[] arr = new int[n];
        System.out.println("Enter the element of the array:");
        for(int i=0;i< n;i++){
            arr[i] = sc.nextInt();
        }
        // insertion Sort
        




















        






















        sc.close();
    }
}